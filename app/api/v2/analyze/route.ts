export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { name, birth, birthHour, gender, relationship, category, planType } = body;

    if (!birth || !name || !category) {
      return NextResponse.json({ error: "필수 정보가 누락되었습니다" }, { status: 400 });
    }

    const scores = {
      total: calcScore(birth, 1),
      wealth: calcScore(birth, 2),
      love: calcScore(birth, 3),
      health: calcScore(birth, 4),
      success: calcScore(birth, 5),
    };

    const [y, m, d] = birth.split("-").map(Number);
    const luckyColor = LUCKY_COLORS[(y + m + d) % LUCKY_COLORS.length];
    const luckyNumber = ((y + m * 13 + d * 7) % 9) + 1;
    const luckyDirection = LUCKY_DIRS[(y + m) % LUCKY_DIRS.length];

    const scoreMap: Record<string, number> = {
      "🌟 오늘의 운세": scores.total,
      "💰 재물운": scores.wealth,
      "💕 연애운": scores.love,
      "💪 건강운": scores.health,
      "🎯 성공운": scores.success,
      "✨ 총운": scores.total,
    };

    // ── 무료: 재물운만 템플릿 (1,000자) ────────────
    if (planType !== "paid") {
      const catLabel = category.replace(/\S+\s/, "");
      const catScore = scoreMap[category] ?? scores.total;
      
      const analysis = getFreeTemplate(name, birth, gender, catLabel, catScore);

      return NextResponse.json({ scores, analysis, luckyColor, luckyNumber, luckyDirection });
    }

    // ── 유료: 4개 카테고리 — 템플릿 (API 호출 없음, 비용 0원) ─────────────────
    const analyses: Record<string, string> = {};
    for (const cat of ALL_CATS) {
      const catLabel = cat.replace(/\S+\s/, "");
      const catScore = scoreMap[cat] ?? 70;
      analyses[cat] = getPaidTemplate(name, birth, gender, catLabel, catScore);
    }

    return NextResponse.json({
      scores,
      analysis: analyses[category] ?? analyses[ALL_CATS[0]],
      allAnalyses: analyses,
      luckyColor,
      luckyNumber,
      luckyDirection,
    });
  } catch (err) {
    console.error("v2/analyze error:", err);
    return NextResponse.json({ error: "분석 중 오류가 발생했습니다" }, { status: 500 });
  }
}

