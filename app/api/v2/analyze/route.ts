async function callHaiku(prompt: string, maxTokens: number): Promise<string> {
  const res = await fetch("https://api.anthropic.com/v1/messages", {
    method: "POST",
    headers: {
      "x-api-key": process.env.ANTHROPIC_API_KEY ?? "",
      "anthropic-version": "2023-06-01",
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      model: "claude-haiku-4-5-20251001",
      max_tokens: maxTokens,
      messages: [{ role: "user", content: prompt }],
    }),
  });
  if (!res.ok) throw new Error(`Anthropic ${res.status}: ${await res.text()}`);
  const data = await res.json();
  return (data.content?.[0]?.text ?? "").trim();
}

function cutAtSentence(text: string, maxLen: number): string {
  if (text.length <= maxLen) return text;
  const endings = ["다.", "요.", "다!", "요!", "다~", "요~", "다...", "요..."];
  let cut = text.slice(0, maxLen);
  let best = -1;
  for (const e of endings) {
    const idx = cut.lastIndexOf(e);
    if (idx > best) best = idx;
  }
  return best > maxLen * 0.5 ? text.slice(0, best + 2) : cut;
}

