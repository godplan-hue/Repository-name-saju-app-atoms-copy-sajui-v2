function calcScore(birth: string, salt: number): number {
  const nums = birth.replace(/-/g, "").split("").map(Number);
  const sum = nums.reduce((a, b) => a + b, 0);
  return Math.min(95, Math.max(55, ((sum * 7 + salt * 13) % 41) + 55));
}

